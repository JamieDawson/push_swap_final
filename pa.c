#include "check_header.h"

void		push_a(t_nums **a, t_nums **b)
{
	t_nums *bot_b;

	if (*b == NULL)
		return ;
	bot_b = (*b)->next;
	(*b)->next = *a;
	*a = *b;
	*b = bot_b;
}
